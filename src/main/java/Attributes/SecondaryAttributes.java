package Attributes;

public class SecondaryAttributes {
    public int Health;
    public int RatingArmor;
    public int Resistance;

    public int getHealth() {
        return Health;
    }

    public void setHealth(int health) {
        Health = health;
    }

    public int getRatingArmor() {
        return RatingArmor;
    }

    public void setRatingArmor(int ratingArmor) {
        RatingArmor = ratingArmor;
    }

    public int getResistance() {
        return Resistance;
    }

    public void setResistance(int resistance) {
        Resistance = resistance;
    }
}
